import hid
import sys
import time

devices = []
for d in hid.enumerate(0x054c, 0x1000):
    devices.append(hid.device(0x054c, 0x1000, d["path"]))

for dev in devices:
    dev.write([0x0,0xFF,0xFF,0xFF,0xFF,0xFF,0x0,0x0])
    time.sleep(0.3)
    dev.write([0x0,0x0,0x0,0x0,0x0,0x0,0x0,0x0])

for dev in devices:
    dev.close()


